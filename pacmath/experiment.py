def _memory_path(out_dir: Path, pair_id: str) -> Path:
    return out_dir / "memory" / f"{pair_id}_memory.json"


def _records_path(out_dir: Path, pair_id: str, split_name: str) -> Path:
    return out_dir / "records" / f"{split_name}_{pair_id}.jsonl"


def _method_rows_path(out_dir: Path, pair_id: str, split_name: str) -> Path:
    return out_dir / "method_rows" / f"{split_name}_{pair_id}_methods.jsonl"


def _adaptive_selector_path(out_dir: Path, pair_id: str) -> Path:
    return out_dir / "selectors" / f"{pair_id}_adaptive_selector.json"


def build_memory_from_records(records: List[Dict[str, Any]], alpha: float) -> ReliabilityMemory:
    mem = ReliabilityMemory(alpha=alpha)
    for rec in records:
        pair_id = rec["pair_id"]
        topic = rec["topic"]
        candidates = rec.get("candidates", [])
        a0 = next((c for c in candidates if c.get("candidate_id") == "A0"), {})
        b0 = next((c for c in candidates if c.get("candidate_id") == "B0"), {})
        initial_any_correct = bool(a0.get("is_correct", False)) or bool(b0.get("is_correct", False))
        for cand in candidates:
            mem.update(
                pair_id=pair_id,
                agent_id=str(cand["agent_id"]),
                model=str(cand["model"]),
                topic=topic,
                stage=str(cand["stage"]),
                correct=bool(cand.get("is_correct", False)),
            )
            mem.update_candidate_policy(
                pair_id=pair_id,
                agent_id=str(cand["agent_id"]),
                model=str(cand["model"]),
                topic=topic,
                stage=str(cand["stage"]),
                candidate_id=str(cand.get("candidate_id", "")),
                correct=bool(cand.get("is_correct", False)),
                initial_any_correct=initial_any_correct,
            )
    return mem


def _current_protocol_version() -> str:
    return str(getattr(config, "PROTOCOL_VERSION", "unknown_protocol"))


def _record_protocol_matches(rec: Dict[str, Any]) -> bool:
    """Return True only when a cached record was generated by this protocol.

    v14 fixed the evidence-gate finalization logic, but old v13 records had the
    same run_id and pair_id. Without a protocol/version check, run_full.py reused
    stale A0/B0/A1/B1 candidates and only recomputed method rows. That made the
    apparent v14 results invalid.
    """
    return str(rec.get("protocol_version", "")) == _current_protocol_version()


def _is_complete_record(rec: Dict[str, Any]) -> bool:
    """A cached record is reusable only if it is complete and protocol-current."""
    candidates = rec.get("candidates") or []
    if rec.get("error"):
        return False
    if not _record_protocol_matches(rec):
        return False
    candidate_ids = {str(c.get("candidate_id")) for c in candidates}
    return {"A0", "B0", "A1", "B1"}.issubset(candidate_ids)


def _latest_record_by_run_id(records: List[Dict[str, Any]]) -> Dict[str, Dict[str, Any]]:
    latest: Dict[str, Dict[str, Any]] = {}
    for rec in records:
        run_id = str(rec.get("run_id", ""))
        if run_id:
            latest[run_id] = rec
    return latest


def _compact_records_file(out_path: Path, rows: List[Dict[str, Any]]) -> List[Dict[str, Any]]:
    """Keep one latest record per run_id and rewrite the JSONL.

    This prevents old timeout records from polluting diagnostics after a later
    successful retry for the same run_id.
    """
    latest = _latest_record_by_run_id(rows)
    compact = sorted(latest.values(), key=lambda r: int(r.get("problem_index", 10**12)))
    write_jsonl(out_path, compact)
    rows_to_csv(out_path.with_suffix(".csv"), compact)
    return compact



def _verification_complete(rec: Dict[str, Any]) -> bool:
    ver = rec.get("verifier") or {}
    if not ver or not ver.get("parse_ok"):
        return False
    ids = {str(r.get("candidate_id")) for r in ver.get("reviews", []) if isinstance(r, dict)}
    return {"A0", "B0", "A1", "B1"}.issubset(ids)


def _enrich_verifier_records(
    records: List[Dict[str, Any]],
    out_path: Path,
    pair_cfg: Dict[str, str],
    client: MultiProviderClient,
    split_name: str,
) -> List[Dict[str, Any]]:
    if not getattr(config, "VERIFIER_ENABLED", False):
        return records
    verifier_model = str(getattr(config, "VERIFIER_MODEL", pair_cfg.get("agent_b_model", "")) or pair_cfg.get("agent_b_model", ""))
    options = dict(getattr(config, "VERIFIER_OPTIONS", config.GENERATION_OPTIONS))
    max_retries = int(getattr(config, "MAX_VERIFIER_RETRIES", config.MAX_JSON_RETRIES))
    changed = False
    need = [r for r in records if r.get("candidates") and not _verification_complete(r)]
    if need:
        print(f"[{split_name} | {pair_cfg['pair_id']}] verifying {len(need)} records with {verifier_model}")
    done = 0
    for rec in records:
        if not rec.get("candidates") or _verification_complete(rec):
            continue
        try:
            rec["verifier"] = verify_candidate_answers(
                client=client,
                verifier_model=verifier_model,
                problem=str(rec.get("problem", "")),
                candidates=rec.get("candidates", []),
                options=options,
                max_retries=max_retries,
            )
        except Exception as e:
            rec["verifier"] = {
                "verifier_model": verifier_model,
                "parse_ok": False,
                "error": repr(e),
                "reviews": [],
                "tokens": {},
                "attempts": 0,
            }
        done += 1
        changed = True
        if done % config.SAVE_EVERY_N_PROBLEMS == 0:
            ok = "ok" if _verification_complete(rec) else "incomplete"
            print(f"[{split_name} | {pair_cfg['pair_id']}] verified {done}/{len(need)} ({ok})")
            write_jsonl(out_path, records)
            rows_to_csv(out_path.with_suffix(".csv"), records)
    if changed:
        write_jsonl(out_path, records)
        rows_to_csv(out_path.with_suffix(".csv"), records)
    return records

def run_split_records(
    split_name: str,
    rows: List[Dict[str, Any]],
    pair_cfg: Dict[str, str],
    out_dir: Path,
    client: MultiProviderClient,
) -> List[Dict[str, Any]]:
    pair_id = pair_cfg["pair_id"]
    out_path = _records_path(out_dir, pair_id, split_name)

    existing_records = read_jsonl(out_path) if out_path.exists() else []
    latest = _latest_record_by_run_id(existing_records)
    completed_ok = {run_id for run_id, rec in latest.items() if _is_complete_record(rec)}

    # Warn clearly when old failed records are going to be retried.
    bad_cached = [run_id for run_id, rec in latest.items() if run_id not in completed_ok]
    stale_cached = [run_id for run_id, rec in latest.items() if rec.get("candidates") and not _record_protocol_matches(rec)]
    if bad_cached:
        if stale_cached:
            print(
                f"[{split_name} | {pair_id}] retrying {len(bad_cached)} cached records "
                f"({len(stale_cached)} stale protocol-version records; current={_current_protocol_version()})"
            )
        else:
            print(f"[{split_name} | {pair_id}] retrying {len(bad_cached)} cached incomplete/error records")

    for i, row in enumerate(rows):
        run_id = f"{pair_id}__{i}"
        if run_id in completed_ok:
            continue
        try:
            rec = run_problem(
                client=client,
                row=row,
                pair_id=pair_id,
                agent_a_model=pair_cfg["agent_a_model"],
                agent_b_model=pair_cfg["agent_b_model"],
                problem_cols=config.PROBLEM_COL_CANDIDATES,
                topic_cols=config.TOPIC_COL_CANDIDATES,
                answer_cols=config.ANSWER_COL_CANDIDATES,
                options=config.GENERATION_OPTIONS,
                max_retries=config.MAX_JSON_RETRIES,
                problem_index=i,
            )
        except Exception as e:
            if config.STOP_ON_OLLAMA_ERROR:
                raise
            rec = {
                "run_id": run_id,
                "protocol_version": _current_protocol_version(),
                "problem_index": i,
                "pair_id": pair_id,
                "agent_a_model": pair_cfg["agent_a_model"],
                "agent_b_model": pair_cfg["agent_b_model"],
                "error": repr(e),
                "candidates": [],
                "topic": "unknown",
                "total_tokens": 0,
                "total_duration_ns": 0,
                "wall_time_seconds": 0.0,
            }
        append_jsonl(out_path, rec)
        if _is_complete_record(rec):
            completed_ok.add(run_id)
        if (i + 1) % config.SAVE_EVERY_N_PROBLEMS == 0:
            status = "ok" if _is_complete_record(rec) else "incomplete"
            print(f"[{split_name} | {pair_id}] completed {i + 1}/{len(rows)} ({status})")

    final_records = read_jsonl(out_path) if out_path.exists() else []
    compact = _compact_records_file(out_path, final_records) if final_records else []

    # Enrich complete candidate records with a no-gold verifier pass if enabled.
    compact = _enrich_verifier_records(compact, out_path, pair_cfg, client, split_name)

    # Return only the requested split range, in row order. Incomplete records are
    # kept for diagnostics but filtered later before method selection.
    wanted_ids = {f"{pair_id}__{i}" for i in range(len(rows))}
    return [r for r in compact if str(r.get("run_id", "")) in wanted_ids]


def _candidate_token_duration(rec: Dict[str, Any], candidate_id: str) -> Dict[str, float]:
    """Return token/model-duration cost for a single candidate generation.

    A0/B0 costs are only the independent solve for that agent.
    A1/B1 costs are the full debate run because producing a post-debate answer
    requires both independent answers, both critique calls, and both revision calls.
    """
    candidates = {c.get("candidate_id"): c for c in rec.get("candidates", [])}
    if candidate_id in {"A0", "B0"}:
        tok = (candidates.get(candidate_id, {}).get("tokens") or {})
        return {
            "method_total_tokens": int(tok.get("total_tokens", 0) or 0),
            "method_duration_seconds": float(tok.get("duration_ns", 0) or 0) / 1e9,
        }
    return {
        "method_total_tokens": int(rec.get("total_tokens", 0) or 0),
        "method_duration_seconds": float(rec.get("total_duration_ns", 0) or 0) / 1e9,
    }


def _method_cost(rec: Dict[str, Any], method_name: str) -> Dict[str, float]:
    """Approximate method-specific compute cost.

    Single-agent baselines should not inherit the full two-agent debate cost.
    All debate, four-candidate, reliability, PAC-Math, and oracle selectors
    require the full A0/B0/critique/revision run, so they use full record cost.
    """
    if method_name == "single_A":
        return _candidate_token_duration(rec, "A0")
    if method_name == "single_B":
        return _candidate_token_duration(rec, "B0")
    total_tokens = int(rec.get("total_tokens", 0) or 0)
    total_duration = float(rec.get("total_duration_ns", 0) or 0) / 1e9
    if method_name.startswith("pac_math_verifier"):
        ver = rec.get("verifier") or {}
        tok = ver.get("tokens") or {}
        total_tokens += int(tok.get("total_tokens", 0) or 0)
        total_duration += float(tok.get("duration_ns", 0) or 0) / 1e9
    return {
        "method_total_tokens": total_tokens,
        "method_duration_seconds": total_duration,
    }


def apply_methods_to_records(
    records: List[Dict[str, Any]],
    memory: Optional[ReliabilityMemory],
    out_path: Path,
    adaptive_selector: Optional[AdaptiveCandidateSelector] = None,
) -> List[Dict[str, Any]]:
    rows: List[Dict[str, Any]] = []
    for rec in records:
        if not rec.get("candidates"):
            continue
        methods = apply_all_methods(rec, memory=memory, adaptive_selector=adaptive_selector)
        for method_name, result in methods.items():
            ann = flip_annotations(rec["candidates"], bool(result["is_correct"]))
            cost = _method_cost(rec, method_name)
            row = {
                "run_id": rec["run_id"],
                "pair_id": rec["pair_id"],
                "problem_index": rec["problem_index"],
                "topic": rec.get("topic", "unknown"),
                "method": method_name,
                # Full two-agent run cost kept for auditing/reproducibility.
                "full_run_total_tokens": rec.get("total_tokens", 0),
                "full_run_wall_time_seconds": rec.get("wall_time_seconds", 0.0),
                "full_run_model_duration_seconds": float(rec.get("total_duration_ns", 0) or 0) / 1e9,
                # Method-specific cost used in summary tables.
                **cost,
                **result,
                **ann,
            }
            rows.append(row)
    write_jsonl(out_path, rows)
    rows_to_csv(out_path.with_suffix(".csv"), rows)
    return rows


def run_experiment(experiment_name: str, calibration_n: Optional[int], test_n: Optional[int]) -> None:
    out_dir = config.OUT_DIR / experiment_name
    out_dir.mkdir(parents=True, exist_ok=True)
    client = build_model_client(config)

    calib_rows = subset_rows(read_jsonl(config.CALIBRATION_FILE), calibration_n)
    test_rows = subset_rows(read_jsonl(config.TEST_FILE), test_n)

    all_method_rows: List[Dict[str, Any]] = []

    for pair_cfg in config.MODEL_PAIRS:
        pair_id = pair_cfg["pair_id"]
        print(f"\n=== Pair: {pair_id} ===")
        print(f"Calibration records: {len(calib_rows)} | Test records: {len(test_rows)}")

        calib_records = run_split_records("calibration", calib_rows, pair_cfg, out_dir, client)
        calib_records_ok = [r for r in calib_records if r.get("candidates")]
        memory = build_memory_from_records(calib_records_ok, alpha=config.ALPHA)
        mem_path = _memory_path(out_dir, pair_id)
        mem_path.parent.mkdir(parents=True, exist_ok=True)
        mem_path.write_text(json.dumps(memory.to_dict(), indent=2, ensure_ascii=False), encoding="utf-8")

        adaptive_selector = None
        if getattr(config, "ADAPTIVE_SELECTOR_ENABLED", True):
            adaptive_selector = AdaptiveCandidateSelector(
                lambda_grid=list(getattr(config, "ADAPTIVE_LAMBDA_GRID", [0.0, 0.25, 0.5, 0.75, 1.0, 1.5, 2.0, 3.0])),
                support_grid=list(getattr(config, "ADAPTIVE_SUPPORT_GRID", [0.0, 0.01, 0.03, 0.05])),
                cross_stage_grid=list(getattr(config, "ADAPTIVE_CROSS_STAGE_GRID", [0.0, 0.02, 0.05])),
                independent_grid=list(getattr(config, "ADAPTIVE_INDEPENDENT_GRID", [0.0, 0.01, 0.03])),
                random_state=int(getattr(config, "PYTHONHASHSEED", 42)),
            ).fit(calib_records_ok, memory)
            selector_path = _adaptive_selector_path(out_dir, pair_id)
            selector_path.parent.mkdir(parents=True, exist_ok=True)
            selector_path.write_text(json.dumps(adaptive_selector.to_dict(), indent=2, ensure_ascii=False), encoding="utf-8")
            print(f"Adaptive selector fitted for {pair_id}: {adaptive_selector.calibration_summary.get('best_params', {})}")

        # Calibration method rows are useful for debugging, but not used as main test evidence.
        apply_methods_to_records(calib_records_ok, memory, _method_rows_path(out_dir, pair_id, "calibration"), adaptive_selector=adaptive_selector)

        test_records = run_split_records("test", test_rows, pair_cfg, out_dir, client)
        test_records_ok = [r for r in test_records if r.get("candidates")]
        method_rows = apply_methods_to_records(test_records_ok, memory, _method_rows_path(out_dir, pair_id, "test"), adaptive_selector=adaptive_selector)
        all_method_rows.extend(method_rows)

    combined_path = out_dir / "combined_test_method_rows.jsonl"
    write_jsonl(combined_path, all_method_rows)
    rows_to_csv(combined_path.with_suffix(".csv"), all_method_rows)
    print(f"\nSaved combined method rows to {combined_path}")
